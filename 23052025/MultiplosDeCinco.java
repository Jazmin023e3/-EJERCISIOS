public class MultiplosDeCinco {
    public static void main(String[] args) {
        // Bucle que va desde 1 hasta 50
        for (int i = 1; i <= 50; i++) {
            int multiplo = i * 5; // Calcula el múltiplo de 5
            System.out.println("Múltiplo " + i + " de 5: " + multiplo);
        }
    }
}